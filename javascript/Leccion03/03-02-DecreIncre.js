let a = 3, b = 2;
let z = 0;
//Incremento
//Pre-Incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);
//Post-Incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);
//Decremento
//Predecremento
z = --a;
console.log(a);
console.log(z);
//Postdecremento
z = b--;
console.log(b);
console.log(z);